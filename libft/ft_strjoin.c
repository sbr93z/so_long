#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int			i;
	char		*s3;
	int			j;

	j = 0;
	i = ft_strlen(s1) + ft_strlen(s2);
	s3 = malloc((i + 1) * sizeof(char));
	if (!s3)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s3[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		s3[i] = s2[j];
		j++;
		i++;
	}
	s3[i] = '\0';
	return (s3);
}
